package bellshade.learn.basic.TipeData;

// tipe data primitif pada java
// byte     5
// short    5
// int      5
// long     5
// float    5.0
// double   5.0
// boolean  true / false
// char     'A'

public class Boolean{
    public static void main(String[] args){
        // boolean adalah tipe data yang memiliki 2 nilai, yaitu benar dan salah
        // nilai benar direpresentasikan dengan true
        // nilai salah direpresentasikan dengan false

        boolean benar = true;
        boolean salah = false;

        System.out.println(benar);
        System.out.println(salah);
    }    
}