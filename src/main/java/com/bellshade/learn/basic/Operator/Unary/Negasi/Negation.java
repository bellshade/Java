package com.bellshade.learn.basic.Operator.Unary.Negasi;

public class Negation {
    public static void main(String[] args) {
        int a, b;
        a = 20;

        System.out.println("Nilai a adalah 20");
        System.out.println("Nilai b adalah Negasi dari nilai a");

        b = -a;
        System.out.println("Nilai b sekarang : " + b);
    }
}
