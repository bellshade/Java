package learn.basic.TipeData;

// tipe data primitif pada java
// byte     5
// short    5
// int      5
// long     5
// float    5.0
// double   5.0
// boolean  true / false
// char     'A'

public class Integer{
  public static void main(String[] args){
    // integer adalah tipe data yang sering dipake dan memiliki
    // value  -2147483648 hingga 2147483647
    // penggunaan memori lebih banyak dari byte dan short

    int angka = 504210;
    System.out.println(angka);
  }
}
